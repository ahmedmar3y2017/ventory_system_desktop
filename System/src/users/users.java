    public void init_table() {
        model = new DefaultTableModel() {

            boolean[] a = {false, false, false};

            @Override
            public boolean isCellEditable(int row, int column) {

                return a[column];

            }

        };
        table.setModel(model);
        model.addColumn("الاسم");
        model.addColumn("الرقم السرى");
        model.addColumn("الموبايل");

    }

    public void retrieve() {

        if (data.re()) {

            try {
                ResultSet res = data.retrieve_users();

                while (res.next()) {

                    model.addRow(new Object[]{res.getString("username"), res.getString("password"), res.getString("tel")});

                }
            } catch (SQLException ex) {
                Logger.getLogger(users.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {

            JOptionPane.showMessageDialog(null, "database error");
        }

    }

    public void do_filter(String name) {

        TableRowSorter<DefaultTableModel> mod = new TableRowSorter<DefaultTableModel>(model);

        table.setRowSorter(mod);

        mod.setRowFilter(RowFilter.regexFilter(name));

    }

