    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed

        int row = table.getSelectedRow();
        if (row == -1) {

            JOptionPane.showMessageDialog(null, "اختر المستخدم  ....");
        } else {

            int number = JOptionPane.showConfirmDialog(null, "هل تريد الخذف ؟؟؟؟");
            if (number == 0) {
                if (data.re()) {

                    try {
                        if (data.delete_user(table.getValueAt(row, 0).toString(), table.getValueAt(row, 1).toString())) {

                            // delete from table
                            model.removeRow(row);

                        } else {
                            JOptionPane.showMessageDialog(null, "database error");
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(users.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Error database");

                }

            }

        }


    }//GEN-LAST:event_deleteActionPerformed

